def search_rules(query: str) -> str:
    """–ü–æ–∏—Å–∫ –ø–æ –±–∞–∑–µ –ø—Ä–∞–≤–∏–ª Arizona RP - —É–ª—É—á—à–µ–Ω–Ω—ã–π –∞–ª–≥–æ—Ä–∏—Ç–º v2"""
    import re
    # Normalization
    query_lower = query.lower().strip()
    
    # Simple translit replacement for common attempts if needed, 
    # but for now let's rely on keywords
    
    # –†–∞–∑–±–∏–≤–∞–µ–º –Ω–∞ —Å–ª–æ–≤–∞, –∏–≥–Ω–æ—Ä–∏—Ä—É—è –ø—É–Ω–∫—Ç—É–∞—Ü–∏—é
    words = re.findall(r'\w+', query_lower)
    
    scores = {}  # –î–ª—è —Ä–∞–Ω–∂–∏—Ä–æ–≤–∞–Ω–∏—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
    
    for key, rule in ARIZONA_RULES.items():
        score = 0
        rule_lower_title = rule.get("title", "").lower()
        keywords = [k.lower() for k in rule.get("keywords", [])]

        # 1. –ü—Ä—è–º–æ–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ —Å –∫–ª—é—á–æ–º (dm, sk, tk)
        if key == query_lower:
            score += 200
        elif key in words:
            score += 100
        elif key in query_lower: # "—á—Ç–æ —Ç–∞–∫–æ–µ –¥–º" -> "–¥–º" inside
             score += 50
        
        # 2. –ü—Ä–æ–≤–µ—Ä–∫–∞ –∫–ª—é—á–µ–≤—ã—Ö —Å–ª–æ–≤
        for keyword in keywords:
            if keyword == query_lower:
                score += 150
            elif keyword in words:
                score += 80
            elif keyword in query_lower:
                score += 40
                
        # 3. –ü—Ä–æ–≤–µ—Ä–∫–∞ –∫–∞–∂–¥–æ–≥–æ —Å–ª–æ–≤–∞ –∑–∞–ø—Ä–æ—Å–∞
        for word in words:
            if len(word) < 2: continue # skip 1 letter words
            
            # –°–æ–≤–ø–∞–¥–µ–Ω–∏–µ —Å –∫–ª—é—á–æ–º
            if word == key:
                score += 50
            
            # –°–æ–≤–ø–∞–¥–µ–Ω–∏–µ —Å –∫–ª—é—á–µ–≤—ã–º–∏ —Å–ª–æ–≤–∞–º–∏
            if word in keywords:
                score += 40
            
            # –°–æ–≤–ø–∞–¥–µ–Ω–∏–µ —Å –∑–∞–≥–æ–ª–æ–≤–∫–æ–º
            if word in rule_lower_title:
                score += 20
        
        if score > 0:
            scores[key] = score
            # print(f"[RULES DEBUG] Candidate '{key}': {score}")
    
    # –°–æ—Ä—Ç–∏—Ä—É–µ–º –ø–æ —Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω–æ—Å—Ç–∏
    sorted_keys = sorted(scores.keys(), key=lambda k: scores[k], reverse=True)
    
    if not sorted_keys:
        return None
        
    best_key = sorted_keys[0]
    best_score = scores[best_key]
    
    if best_score >= 30: # Slightly lowered threshold
        return ARIZONA_RULES[best_key]['content']
            
    # –ï—Å–ª–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç —Å–ª–∞–±—ã–π, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º None, –ø—É—Å—Ç—å AI –¥—É–º–∞–µ—Ç
    return None

